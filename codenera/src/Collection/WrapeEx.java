package Collection;

public class WrapeEx 
{

	public static void main(String[] args) 
	{
		int a=10;//primitive 
		Integer i=a;//non-primitive
		System.out.println(i);//Autoboxing
		
		Integer i1=10;//non-primitive
		int a1=i1;//primitive
		System.out.println(a1);//unboxing
		
		Character obj='a';
		char ch=obj;
		System.out.println(ch);
		
		
		
		
	}

}
